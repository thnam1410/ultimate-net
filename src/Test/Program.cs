using Microsoft.AspNetCore.Http.HttpResults;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
// Learn more about configuring OpenAPI at https://aka.ms/aspnet/openapi
builder.Services.AddOpenApi();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.MapOpenApi();
}
app.MapTestAPI();

app.UseHttpsRedirection();

app.Run();

public record WeatherForecast(DateOnly Date, int TemperatureC, string? Summary)
{
    public int TemperatureF => 32 + (int)(TemperatureC / 0.5556);
}

public static class TestAPI
{
    public static IEndpointRouteBuilder MapTestAPI(this IEndpointRouteBuilder app)
    {
        var api = app.MapGroup("weatherforecast");

        app.MapGet("/", GetTest);
        return app;
    }

    public static async Task<Results<Ok<WeatherForecast[]>, BadRequest<string>>> GetTest(HttpContext context)
    {
        await Task.CompletedTask;
        var summaries = new[]
        {
            "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
        };
        
        var forecast = Enumerable.Range(1, 5).Select(index =>
                new WeatherForecast
                (
                    DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                    Random.Shared.Next(-20, 55),
                    summaries[Random.Shared.Next(summaries.Length)]
                ))
            .ToArray();
        return TypedResults.Ok(forecast);
    }
}